Future<double> pertanyaanCertaintyFactor(int jenisGangguan, List jawaban) {
  List cfGejala = [];
  double cfCombined;

  for(int i = 0; i < jawaban.length; i++) {
    cfGejala.add(jawaban[i][1] * daftarBobotPakar[jenisGangguan][i]);
  }

  for(int i = 0; i < jawaban.length - 1; i++) {
    if(i == 0) {
      cfCombined = cfGejala[i] + cfGejala[i+1] * (1 - cfGejala[i]);
    } else {
      cfCombined = cfCombined + cfGejala[i+1] * (1 - cfCombined);
    }
  }

  return Future.value(cfCombined);
}